print("=== Проверка совместимости групп крови ===\n")
donor = input("Введите группу крови донора (O, A, B, AB): ").upper()
patient = input("Введите группу крови пациента (O, A, B, AB): ").upper()

if donor == patient or donor == "O":
    print(f"\nПереливание ВОЗМОЖНО: кровь группы {donor} подходит пациенту с группой {patient}.")
else:
    print(f"\nПереливание НЕВОЗМОЖНО: кровь группы {donor} нельзя переливать пациенту с группой {patient}.")
