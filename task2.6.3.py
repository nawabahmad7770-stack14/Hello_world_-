print("=== Витязь на распутье ===")
print("Перед вами камень, а на нем три пути. Куда путь держишь, молодец?")
print("1. Налево\n2. Направо\n3. Прямо")

choice = input("\nТвой выбор (введи направление): ")

if choice == "налево" or choice == "1":
    print("Результат: Богат ты будешь, но жизнь твою поглотят злато и скука.")

elif choice == "направо" or choice == "2":
    print("Результат: Женат ты будешь, познаешь радость семейную, но о подвигах забудешь.")

elif choice == "прямо" or choice == "3":
    print("Результат: Живу не бывати — коня потеряешь, а сам славу вечную в бою обретешь.")

else:
    print("Нет такой дороги на камне. Стой и думай дальше, пока вороны не заклевали.")
