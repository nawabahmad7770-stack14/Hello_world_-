#!/bin/bash

echo "=== Анализ использования дискового пространства ==="
echo "Файловая система | Заполнение"
echo "----------------|-----------"

df -h | awk 'NR > 1 {
    percent = $5 + 0
    if (percent > 90) {
        print $1, $5" *** ВНИМАНИЕ: КРИТИЧЕСКИЙ УРОВЕНЬ! ***"
    } else {
        print $1, $5
    }
}'   
